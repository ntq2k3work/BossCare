export type Copy = typeof i18n.vi;

export function normalizeLocale(value: string | null | undefined): Locale {
  return value === "en" ? "en" : "vi";
}

export function getCopy(locale: Locale): Copy {
  return (i18n[locale] ?? i18n.vi) as Copy;
}

export function formatDate(locale: Locale, value: string | null | undefined) {
  if (!value) return getCopy(locale).formats.notDue;
  return new Intl.DateTimeFormat(locale === "vi" ? "vi-VN" : "en-US", {
    day: "2-digit",
    month: "2-digit",
    year: "numeric",
  }).format(new Date(value));
}

export function formatDateTime(locale: Locale, value: string) {
  return new Intl.DateTimeFormat(locale === "vi" ? "vi-VN" : "en-US", {
    day: "2-digit",
    month: "2-digit",
    year: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  }).format(new Date(value));
}

export function formatStorageLimit(locale: Locale, value: number) {
  const copy = getCopy(locale);
  return value >= 1024 ? copy.formats.storageGB(value) : copy.formats.storageMB(value);
}

export function formatPlanLabel(locale: Locale, plan: string) {
  const copy = getCopy(locale);
  if (plan === "plus") return copy.statuses.plus;
  if (plan === "family") return copy.statuses.family;
  return copy.statuses.free;
}

export function formatRoleLabel(locale: Locale, role: string | null | undefined) {
  const copy = getCopy(locale);
  if (!role) return copy.common.noData;
  const normalized = role.toUpperCase();
  if (normalized === "OWNER") return copy.statuses.owner;
  if (normalized === "MEMBER") return copy.statuses.member;
  return role;
}

export function formatSpecies(locale: Locale, species: string) {
  const normalized = species.toLowerCase();
  if (normalized.includes("dog") || normalized.includes("chó")) {
    return getCopy(locale).formats.dog;
  }
  if (normalized.includes("cat") || normalized.includes("mèo")) {
    return getCopy(locale).formats.cat;
  }
  return species;
}

export function formatSex(locale: Locale, sex: string | null | undefined) {
  const copy = getCopy(locale);
  if (!sex) return copy.formats.unknown;
  const normalized = sex.toLowerCase();
  if (normalized.includes("female") || normalized.includes("cái")) {
    return copy.formats.female;
  }
  if (normalized.includes("male") || normalized.includes("đực") || normalized.includes("duc")) {
    return copy.formats.male;
  }
  return sex;
}

export function formatPetAge(locale: Locale, pet: { estimatedAge?: string | null; birthdate?: string | null }, now = new Date()) {
  const copy = getCopy(locale);
  if (pet.estimatedAge) return pet.estimatedAge;
  if (!pet.birthdate) return copy.formats.noAge;
  const birthdate = new Date(`${pet.birthdate}T00:00:00.000Z`);
  const months = Math.max(
    0,
    (now.getUTCFullYear() - birthdate.getUTCFullYear()) * 12 + now.getUTCMonth() - birthdate.getUTCMonth(),
  );
  const years = Math.floor(months / 12);
  const remainingMonths = months % 12;
  if (years && remainingMonths) return copy.formats.yearsMonths(years, remainingMonths);
  if (years) return copy.formats.years(years);
  return copy.formats.months(remainingMonths || 1);
}

export function daysUntil(value: string, now = new Date()) {
  const target = new Date(`${value.slice(0, 10)}T00:00:00.000Z`);
  const start = new Date(Date.UTC(now.getUTCFullYear(), now.getUTCMonth(), now.getUTCDate()));
  return Math.ceil((target.getTime() - start.getTime()) / 86_400_000);
}

export function formatDue(locale: Locale, value: string | null | undefined, now = new Date()) {
  const copy = getCopy(locale);
  if (!value) return copy.formats.notDue;
  const days = daysUntil(value, now);
  if (days < 0) return copy.formats.overdue(Math.abs(days));
  if (days === 0) return copy.formats.today;
  return copy.formats.remaining(days);
}

export function formatPaymentStatus(locale: Locale, status: string) {
  const copy = getCopy(locale);
  const label = copy.statuses[status as keyof typeof copy.statuses];
  return typeof label === "string" ? label : status;
}

export function formatTransactionStatus(locale: Locale, status: string) {
  return formatPaymentStatus(locale, status);
}

